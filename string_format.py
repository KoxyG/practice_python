#converting a string to a upper, lower, capitalize or tittle case

name = "wind in the willows"
print(name.upper())
print(name.lower())
print(name.capitalize())
print(name.title())

#formatting a string with escape character \n
statement = "My name is koxy.\nI'm a software developer\nWhat's your profession?"
print(statement) #prints what's inside statement
print("statement\n\n") #prints the word statement

#formating using tabs
statement = "koxy.\tis a software developer.\tWhat's your profession?"
print(statement) #prints what's inside statement
print("statement") #prints the word statement

